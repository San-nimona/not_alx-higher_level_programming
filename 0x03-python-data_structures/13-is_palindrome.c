/**
 * reverse_listint - linked list revrsal
 * @head: first node in the list
 * Return: the new lis
 */
void reverse_listint(listint_t **head)
{
	listint_t *prev = NULL, *current = *head, *next = NULL;

	while (current)
	{
		next = current->next;
		current->next = prev;
		prev = current;
		current = next;
	}

	*head = prev;
}

